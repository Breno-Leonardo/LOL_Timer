package com.onerb.timerlol.api;

public class Perks {
    int[] perkIds;
    int perkStyle;
    int perkSubStyle;

    public int[] getPerkIds() {
        return perkIds;
    }

    public void setPerkIds(int[] perkIds) {
        this.perkIds = perkIds;
    }

    public int getPerkStyle() {
        return perkStyle;
    }

    public void setPerkStyle(int perkStyle) {
        this.perkStyle = perkStyle;
    }

    public int getPerkSubStyle() {
        return perkSubStyle;
    }

    public void setPerkSubStyle(int perkSubStyle) {
        this.perkSubStyle = perkSubStyle;
    }
}
